function printPyramid($n, $num)
{
    if($n>0)
    {
        echo str_repeat(" ", ($n-1));
        if($n==$num){
          echo "*";
        }else{
          echo str_repeat("*", ($num - $n + 1));
        }
        echo PHP_EOL;
        printPyramid($n - 1, ($num + 1));
    }
}

$n = 25;
printPyramid($n, $n);


